import { readFileSync, writeFileSync } from "fs";
import { resolve } from "path";

const SPONSORS_URL = "https://mavicat.kailingteck.com/sponsors.json";
const ASSETS_BASE = "https://mavicat.kailingteck.com";

const paths = {
  sponsorsMd: resolve("SPONSORS.md"),
  readme: resolve("README.md"),
};

// --- Fetch sponsors from mavicat.kailingteck.com ---
console.log(`🌐 Fetching sponsors from ${SPONSORS_URL}...`);

const res = await fetch(SPONSORS_URL);
if (!res.ok) {
  console.error(`❌ Failed to fetch sponsors: ${res.status} ${res.statusText}`);
  process.exit(1);
}

const sponsors = await res.json();
console.log(`🔄 Syncing ${sponsors.length} sponsors...`);

function withUtm(url) {
  const u = new URL(url);
  u.searchParams.set("utm_source", "mavicat");
  u.searchParams.set("utm_medium", "referral");
  u.searchParams.set("utm_campaign", "sponsor");
  return u.toString();
}

function assetUrl(path) {
  return `${ASSETS_BASE}${path}`;
}

// --- Generate SPONSORS.md ---
const lines = [
  "# Sponsors",
  "",
  "Mavicat is made possible thanks to the support of our sponsors.",
  "Interested in sponsoring? [Get in touch →](https://mavicat.kailingteck.com/sponsors)",
  "",
  "---",
  "",
];

for (const s of sponsors) {
  lines.push(`## ${s.name}`);
  lines.push("");

  if (s.logoImg) {
    const logoPath = assetUrl(s.logoImg);
    const bg = s.logoImgBg ? ` style="background:${s.logoImgBg};padding:6px;"` : "";
    lines.push(
      `<a href="${withUtm(s.url)}" target="_blank"><img src="${logoPath}" height="40" alt="${s.name}"${bg} /></a>`,
    );
    lines.push("");
  }

  lines.push(`**${s.tagline}**`);
  lines.push("");
  lines.push(`🔗 [${s.url}](${withUtm(s.url)})`);
  lines.push("");

  if (s.features && s.features.length > 0) {
    for (const f of s.features) {
      lines.push(`- ${f.icon} ${f.text}`);
    }
    lines.push("");
  }

  if (s.offer) {
    lines.push(`> **${s.offer.title}**`);
    lines.push(`> ${s.offer.description}`);
    lines.push("");
  }

  lines.push("---");
  lines.push("");
}

lines.push(
  "_This file is auto-generated by `scripts/sync-sponsors.js`. Do not edit manually._",
);

writeFileSync(paths.sponsorsMd, lines.join("\n"), "utf-8");
console.log("✅ Generated SPONSORS.md");

// --- Update README.md ---
const START = "<!-- SPONSORS:START -->";
const END = "<!-- SPONSORS:END -->";

const readmeBlock = [
  START,
  "",
  "## Sponsors",
  "",
  ...sponsors.map((s) => {
    const logo = s.logoImgCompact ?? s.logoImg;
    const imgTag = logo
      ? `<a href="${withUtm(s.url)}" target="_blank"><img src="${assetUrl(logo)}" height="28" alt="${s.name}" /></a> `
      : "";
    return `- ${imgTag}**[${s.name}](${withUtm(s.url)})** — ${s.tagline}`;
  }),
  "",
  "_[Become a sponsor →](https://mavicat.kailingteck.com/sponsors)_",
  "",
  END,
].join("\n");

let readme = readFileSync(paths.readme, "utf-8");

if (readme.includes(START) && readme.includes(END)) {
  readme = readme.replace(new RegExp(`${START}[\\s\\S]*?${END}`), readmeBlock);
} else {
  // Insert before ## License
  readme = readme.replace("## License", `${readmeBlock}\n\n## License`);
}

writeFileSync(paths.readme, readme, "utf-8");
console.log("✅ Updated README.md");
